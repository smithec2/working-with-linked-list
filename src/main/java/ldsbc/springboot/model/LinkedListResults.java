public class LinkedListResults {
  private int size;
  private String first;
  private String last;
  
  public int getSize() {
    return size;
  }
  
  public void setSize(int size) {
    this.size = size;
  }
  
  public String getFirst() {
    return first;
  }
  
  public void setFirst(String first) {
    this.first = first;
  }
  
  public String getLast() {
    return last;
  }
  
  public void setLast(String last) {
    this.last = last;
  }
}
